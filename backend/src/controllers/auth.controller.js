export const refreshTokenController = async (req, res, next) => {
  try {
    const refreshToken = req.cookies.refreshToken;

    if (!refreshToken) {
      return res.status(401).json({ message: "Unauthorized" });
    }

    const decoded = jwt.verify(refreshToken, getJwtRefreshSecret());
    let user = await User.findById(decoded.userId).select("-password");

    if (!user) {
      clearAuthCookies(res);
      return res.status(401).json({ message: "Unauthorized" });
    }

    if (typeof decoded.tokenVersion === "number" && user.tokenVersion !== decoded.tokenVersion) {
      clearAuthCookies(res);
      return res.status(401).json({ message: "Unauthorized" });
    }

    const session = Array.isArray(user.refreshSessions)
      ? user.refreshSessions.find(
          (s) => s && s.tokenId === decoded.tokenId && !s.revokedAt && (!s.expiresAt || s.expiresAt > new Date())
        )
      : null;

    if (!session || session.tokenHash !== sha256(refreshToken)) {
      clearAuthCookies(res);
      return res.status(401).json({ message: "Unauthorized" });
    }

    user = await ensureUserHasUsername(user);

    const tokens = generateAuthTokens({ userId: user._id, tokenVersion: user.tokenVersion });

    session.revokedAt = new Date();
    session.replacedByTokenId = tokens.refreshTokenId;
    user.refreshSessions = Array.isArray(user.refreshSessions) ? user.refreshSessions : [];
    user.refreshSessions.push({
      tokenId: tokens.refreshTokenId,
      tokenHash: tokens.refreshTokenHash,
      expiresAt: tokens.refreshExpiresAt,
      ip: getRequestIp(req),
      userAgent: getUserAgent(req),
    });
    user.refreshSessions = user.refreshSessions.slice(-20);
    await user.save();
    await syncRegisteredUser(user);

    setAuthCookies(res, tokens);

    res.status(200).json(sanitizeUser(user));
  } catch (error) {
    if (error.name === "JsonWebTokenError" || error.name === "TokenExpiredError") {
      clearAuthCookies(res);
      return res.status(401).json({ message: "Unauthorized" });
    }

    next(error);
  }
};

export const getSessions = async (req, res, next) => {
  try {
    const userId = req.user?._id;
    if (!userId) return res.status(401).json({ message: "Unauthorized" });

    const user = await User.findById(userId).select("refreshSessions tokenVersion");
    if (!user) return res.status(401).json({ message: "Unauthorized" });

    const now = new Date();
    const sessions = Array.isArray(user.refreshSessions) ? user.refreshSessions : [];

    let currentTokenId = null;
    const refreshToken = req.cookies?.refreshToken;
    if (refreshToken) {
      try {
        const decoded = jwt.verify(refreshToken, getJwtRefreshSecret());
        if (String(decoded.userId) === String(userId)) currentTokenId = decoded.tokenId || null;
      } catch {
        currentTokenId = null;
      }
    }

    const visible = sessions
      .filter((session) => session && (!session.expiresAt || session.expiresAt > now))
      .map((session) => ({
        tokenId: session.tokenId,
        createdAt: session.createdAt,
        expiresAt: session.expiresAt,
        revokedAt: session.revokedAt,
        ip: session.ip,
        userAgent: session.userAgent,
        current: currentTokenId ? session.tokenId === currentTokenId : false,
      }))
      .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());

    res.status(200).json({ sessions: visible });
  } catch (error) {
    next(error);
  }
};

export const logoutDevice = async (req, res, next) => {
  try {
    const userId = req.user?._id;
    if (!userId) return res.status(401).json({ message: "Unauthorized" });

    const tokenId = sanitizePlainText(req.body?.tokenId, { maxLength: 100 });
    if (!tokenId) return res.status(400).json({ message: "tokenId is required" });

    const user = await User.findById(userId);
    if (!user) return res.status(401).json({ message: "Unauthorized" });

    const session = Array.isArray(user.refreshSessions)
      ? user.refreshSessions.find((s) => s && s.tokenId === tokenId)
      : null;

    if (!session) return res.status(404).json({ message: "Session not found" });

    if (!session.revokedAt) {
      session.revokedAt = new Date();
      await user.save();
    }

    // If the user revoked the current device session, also clear cookies.
    const refreshToken = req.cookies?.refreshToken;
    if (refreshToken) {
      try {
        const decoded = jwt.verify(refreshToken, getJwtRefreshSecret());
        if (decoded.tokenId === tokenId) {
          clearAuthCookies(res);
        }
      } catch {
        // ignore
      }
    }

    res.status(200).json({ message: "Device logged out" });
  } catch (error) {
    next(error);
  }
};
