const sha256 = (value) => crypto.createHash("sha256").update(value).digest("hex");
const USERNAME_REGEX = /^[a-z0-9_.]+$/;
const RESERVED_USERNAMES = new Set(["admin", "support", "linepe", "system"]);
const OTP_REGEX = /^\d{6}$/;

const sanitizeUser = (user) => ({
  _id: user._id,
  fullName: user.fullName,
  email: user.email,
  username: user.username || "",
  usernameAutoGenerated: Boolean(user.usernameAutoGenerated),
  phoneNumber: user.phoneNumber || "",
  isPhoneVerified: Boolean(user.isPhoneVerified),
  profilePic: user.profilePic,
  createdAt: user.createdAt,
  updatedAt: user.updatedAt,
});

const normalizeUsername = (value) =>
  sanitizePlainText(value, { maxLength: 30 })
    .trim()
    .toLowerCase();

const validateUsername = (username) => {
  if (!username) return "Username is required";
  if (username.length < 3 || username.length > 30) {
    return "Username must be between 3 and 30 characters";
  }
  if (!USERNAME_REGEX.test(username)) {
    return "Username can only contain lowercase letters, numbers, underscores, and periods";
  }
  if (RESERVED_USERNAMES.has(username)) {
    return "That username is reserved";
  }
  return null;
};

const findUsernameOwner = async (username) => {
  if (!username) return null;
  return User.findOne({ username }).select("_id");
};

const findPhoneOwner = async (phoneNumber) => {
  if (!phoneNumber) return null;
  return User.findOne({ phoneNumber }).select("_id");
};

const normalizeIndianPhoneNumber = (value) => {
  const next = sanitizePlainText(value, { maxLength: 20 });
  if (!next) return "";

  const digitsOnly = next.replace(/\D/g, "");

  if (digitsOnly.length === 10) {
    return `+91${digitsOnly}`;
  }

  if (digitsOnly.length === 12 && digitsOnly.startsWith("91")) {
    return `+${digitsOnly}`;
  }

  if (next.startsWith("+91") && digitsOnly.length === 12 && digitsOnly.startsWith("91")) {
    return `+${digitsOnly}`;
  }

  return "";
};

const getRequestIp = (req) => (typeof req.ip === "string" ? req.ip : "");
const getUserAgent = (req) => (typeof req.get === "function" ? req.get("user-agent") || "" : "");

const addRefreshSession = async (user, tokens, req) => {
  const now = new Date();
  const sessions = Array.isArray(user.refreshSessions) ? user.refreshSessions : [];

  const cleaned = sessions.filter((session) => {
    if (!session) return false;
    if (session.revokedAt) return false;
    if (session.expiresAt && session.expiresAt <= now) return false;
    return true;
  });

  cleaned.push({
    tokenId: tokens.refreshTokenId,
    tokenHash: tokens.refreshTokenHash,
    expiresAt: tokens.refreshExpiresAt,
    ip: getRequestIp(req),
    userAgent: getUserAgent(req),
  });

  // Keep the most recent sessions only.
  user.refreshSessions = cleaned.slice(-20);
  await user.save();
};

export const signup = async (req, res, next) => {
  try {
    const { fullName, email, password, username, phoneNumber } = req.body;

    const nextFullName = sanitizePlainText(fullName, { maxLength: 80 });
    const nextEmail = sanitizePlainText(email, { maxLength: 254 }).toLowerCase();
    const nextUsername = normalizeUsername(username);
    const nextPhoneNumber = typeof phoneNumber === "string" ? normalizeIndianPhoneNumber(phoneNumber) : "";

    if (!nextFullName || !nextEmail || !password || !nextUsername) {
      return res.status(400).json({ message: "Full name, email, username, and password are required" });
    }

    if (password.length < 12) {
      return res.status(400).json({ message: "Password must be at least 12 characters" });
    }

    const usernameError = validateUsername(nextUsername);
    if (usernameError) {
      return res.status(400).json({ message: usernameError });
    }

    if (typeof phoneNumber === "string" && !nextPhoneNumber) {
      return res.status(400).json({ message: "Phone number must be a valid +91 mobile number" });
    }

    const [existingEmailUser, existingUsernameUser, existingPhoneUser] = await Promise.all([
      User.findOne({ email: nextEmail }),
      findUsernameOwner(nextUsername),
      findPhoneOwner(nextPhoneNumber),
    ]);

    if (existingEmailUser) {
      return res.status(400).json({ message: "Email already exists" });
    }
    if (existingUsernameUser) {
      return res.status(400).json({ message: "Username already exists" });
    }
    if (existingPhoneUser) {
      return res.status(400).json({ message: "Phone number already exists" });
    }

    const salt = await bcrypt.genSalt(10);
    const hashedPassword = await bcrypt.hash(password, salt);

    const { otp, expiresAt } = createPendingSignup(nextEmail, {
      fullName: nextFullName,
      email: nextEmail,
      username: nextUsername,
      usernameAutoGenerated: false,
      ...(nextPhoneNumber ? { phoneNumber: nextPhoneNumber } : {}),
      password: hashedPassword,
    });

    await sendEmail({
      to: nextEmail,
      subject: "Your LinePe verification code",
      text: `Your LinePe verification code is ${otp}. It expires in 10 minutes.`,
      html: `
        <div style="font-family:Arial,sans-serif;line-height:1.5;color:#111">
          <h2>Verify your LinePe email</h2>
          <p>Your verification code is:</p>
          <p style="font-size:28px;font-weight:700;letter-spacing:6px">${otp}</p>
          <p>This code expires in 10 minutes.</p>
        </div>
      `,
    });

    res.status(200).json({
      success: true,
      requiresEmailVerification: true,
      email: nextEmail,
      expiresAt,
      message: "Verification code sent to your email",
    });
  } catch (error) {
    if (error?.code === 11000) {
      if (error?.keyPattern?.username) {
        return res.status(400).json({ message: "Username already exists" });
      }
      if (error?.keyPattern?.email) {
        return res.status(400).json({ message: "Email already exists" });
      }
      if (error?.keyPattern?.phoneNumber) {
        return res.status(400).json({ message: "Phone number already exists" });
      }
    }
    next(error);
  }
};

export const verifySignupOtp = async (req, res, next) => {
  try {
    const nextEmail = sanitizePlainText(req.body?.email, { maxLength: 254 }).toLowerCase();
    const otp = sanitizePlainText(req.body?.otp, { maxLength: 6 });

    if (!nextEmail || !OTP_REGEX.test(otp)) {
      return res.status(400).json({ message: "Valid email and 6 digit OTP are required" });
    }

    const pending = consumePendingSignup(nextEmail, otp);
    if (!pending.ok) {
      return res.status(400).json({ message: pending.message });
    }

    const { email, username, phoneNumber } = pending.payload;
    const [existingEmailUser, existingUsernameUser, existingPhoneUser] = await Promise.all([
      User.findOne({ email }),
      findUsernameOwner(username),
      findPhoneOwner(phoneNumber),
    ]);

    if (existingEmailUser) {
      return res.status(400).json({ message: "Email already exists" });
    }
    if (existingUsernameUser) {
      return res.status(400).json({ message: "Username already exists" });
    }
    if (existingPhoneUser) {
      return res.status(400).json({ message: "Phone number already exists" });
    }

    const newUser = await User.create(pending.payload);
    await syncRegisteredUser(newUser);

    const tokens = generateAuthTokens({
      userId: newUser._id,
      tokenVersion: newUser.tokenVersion,
    });
    await addRefreshSession(newUser, tokens, req);
    setAuthCookies(res, tokens);

    enqueueNotification("user:welcome", { userId: String(newUser._id), email: newUser.email }).catch(() => {});

    res.status(201).json(sanitizeUser(newUser));
  } catch (error) {
    if (error?.code === 11000) {
      if (error?.keyPattern?.username) {
        return res.status(400).json({ message: "Username already exists" });
      }
      if (error?.keyPattern?.email) {
        return res.status(400).json({ message: "Email already exists" });
      }
      if (error?.keyPattern?.phoneNumber) {
        return res.status(400).json({ message: "Phone number already exists" });
      }
    }
    next(error);
  }
};

export const login = async (req, res, next) => {
  try {
    const { email, password } = req.body;

    const nextEmail = sanitizePlainText(email, { maxLength: 254 }).toLowerCase();

    if (!nextEmail || !password) {
      return res.status(400).json({ message: "Email and password are required" });
    }

    let user = await User.findOne({ email: nextEmail });

    if (!user) {
      return res.status(400).json({ message: "Invalid credentials" });
    }

    const isPasswordCorrect = await bcrypt.compare(password, user.password);
    if (!isPasswordCorrect) {
      return res.status(400).json({ message: "Invalid credentials" });
    }

    user = await ensureUserHasUsername(user);

    const tokens = generateAuthTokens({ userId: user._id, tokenVersion: user.tokenVersion });
    await addRefreshSession(user, tokens, req);
    setAuthCookies(res, tokens);
    await syncRegisteredUser(user);

    res.status(200).json(sanitizeUser(user));
  } catch (error) {
    next(error);
  }
};

export const logout = (req, res, next) => {
  try {
    const refreshToken = req.cookies?.refreshToken;

    if (refreshToken) {
      try {
        const decoded = jwt.verify(refreshToken, getJwtRefreshSecret());
        User.updateOne(
          { _id: decoded.userId, "refreshSessions.tokenId": decoded.tokenId },
          { $set: { "refreshSessions.$.revokedAt": new Date() } }
        ).catch(() => {});
      } catch {
        // Ignore invalid/expired refresh tokens; cookie clearing is enough.
      }
    }

    clearAuthCookies(res);
    res.status(200).json({ message: "Logged out successfully" });
  } catch (error) {
    next(error);
  }
};

export const logoutAll = async (req, res, next) => {
  try {
    const userId = req.user?._id;
    if (!userId) {
      return res.status(401).json({ message: "Unauthorized" });
    }

    const now = new Date();
    const user = await User.findById(userId);
    if (!user) {
      clearAuthCookies(res);
      return res.status(401).json({ message: "Unauthorized" });
    }

    user.tokenVersion = (user.tokenVersion || 0) + 1;
    if (Array.isArray(user.refreshSessions)) {
      user.refreshSessions.forEach((session) => {
        if (!session.revokedAt) session.revokedAt = now;
      });
    }
    await user.save();

    clearAuthCookies(res);
    res.status(200).json({ message: "Logged out from all devices" });
  } catch (error) {
    next(error);
  }
};

export const updateProfile = async (req, res, next) => {
  try {
    const { profilePic, username, phoneNumber } = req.body;
    const userId = req.user._id;

    const nextUsername = typeof username === "string" ? normalizeUsername(username) : null;
    const hasPhoneNumber = typeof phoneNumber === "string";
    const nextPhoneNumber = hasPhoneNumber ? normalizeIndianPhoneNumber(phoneNumber) : null;
    const updates = {};

    if (!profilePic && typeof username !== "string" && !hasPhoneNumber) {
      return res.status(400).json({ message: "Profile pic, username, or phone number is required" });
    }

    if (typeof username === "string") {
      const usernameError = validateUsername(nextUsername);
      if (usernameError) {
        return res.status(400).json({ message: usernameError });
      }

      const existingUsernameUser = await findUsernameOwner(nextUsername);
      if (existingUsernameUser && String(existingUsernameUser._id) !== String(userId)) {
        return res.status(400).json({ message: "Username already exists" });
      }

      updates.username = nextUsername;
      updates.usernameAutoGenerated = false;
    }

    if (hasPhoneNumber) {
      if (!nextPhoneNumber) {
        return res.status(400).json({ message: "Phone number must be a valid +91 mobile number" });
      }

      const existingPhoneUser = await findPhoneOwner(nextPhoneNumber);
      if (existingPhoneUser && String(existingPhoneUser._id) !== String(userId)) {
        return res.status(400).json({ message: "Phone number already exists" });
      }

      updates.phoneNumber = nextPhoneNumber;
      if (nextPhoneNumber !== (req.user?.phoneNumber || "")) {
        updates.isPhoneVerified = false;
      }
    }

    if (profilePic) {
      const uploadResponse = await cloudinary.uploader.upload(profilePic);
      updates.profilePic = uploadResponse.secure_url;
    }

    const updatedUser = await User.findByIdAndUpdate(userId, updates, { new: true }).select("-password");
    await syncRegisteredUser(updatedUser);

    res.status(200).json(updatedUser);
  } catch (error) {
    if (error?.code === 11000 && error?.keyPattern?.username) {
      return res.status(400).json({ message: "Username already exists" });
    }
    if (error?.code === 11000 && error?.keyPattern?.phoneNumber) {
      return res.status(400).json({ message: "Phone number already exists" });
    }
    next(error);
  }
};

