import User from "../models/user.model.js";
import { upsertRegisteredUser } from "./postgres.js";

const RESERVED_USERNAMES = new Set(["admin", "support", "linepe", "system"]);

const randomSuffix = () => Math.random().toString(36).slice(2, 8);

const slugifyUsernameBase = (value) => {
  const raw = String(value || "")
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9_.\s]/g, " ")
    .replace(/\s+/g, ".")
    .replace(/\.{2,}/g, ".")
    .replace(/^\.|\.$/g, "");

  const safe = raw || "linepe.user";
  return safe.slice(0, 24);
};

const buildUsernameCandidate = (base) => {
  const trimmedBase = slugifyUsernameBase(base);
  const suffix = randomSuffix();
  const maxBaseLength = Math.max(3, 30 - suffix.length - 1);
  return `${trimmedBase.slice(0, maxBaseLength)}.${suffix}`;
};

const usernameExists = async (username, currentUserId) => {
  const existing = await User.findOne({ username }).select("_id");
  if (!existing) return false;
  return String(existing._id) !== String(currentUserId || "");
};

export const ensureUserHasUsername = async (user) => {
  if (!user) return user;

  if (user.username && !RESERVED_USERNAMES.has(String(user.username).toLowerCase())) {
    if (typeof user.usernameAutoGenerated !== "boolean") {
      user.usernameAutoGenerated = false;
      await user.save();
    }
    await upsertRegisteredUser(user);
    return user;
  }

  const base =
    user.fullName ||
    String(user.email || "").split("@")[0] ||
    "linepe.user";

  for (let attempt = 0; attempt < 20; attempt += 1) {
    const candidate = buildUsernameCandidate(base);
    if (RESERVED_USERNAMES.has(candidate)) continue;
    const taken = await usernameExists(candidate, user._id);
    if (taken) continue;

    user.username = candidate;
    user.usernameAutoGenerated = true;
    await user.save();
    await upsertRegisteredUser(user);
    return user;
  }

  throw new Error("Failed to generate a unique username");
};

export const syncRegisteredUser = async (user) => {
  if (!user) return;
  await upsertRegisteredUser(user);
};

export const backfillRegisteredUsers = async () => {
  const users = await User.find({});
  let migrated = 0;

  for (const user of users) {
    const nextUser = await ensureUserHasUsername(user);
    await syncRegisteredUser(nextUser);
    migrated += 1;
  }

  return { migrated };
};
